    private void bCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCadastrarActionPerformed
        // TODO add your handling code here:
        float auxganho=0;
        if(Integer.parseInt(tID.getText())>=0){
            int auxcargo=Main.ids.get(Integer.parseInt(tID.getText())).cargo;
            System.err.println(Integer.parseInt(tID.getText()));
            if(Float.parseFloat(tSalario.getText())>0){
                switch(auxcargo){
                    case 1:
                        Main.ids.get(Integer.parseInt(tID.getText())).chefe.setSalariobase(Float.parseFloat(tSalario.getText()));
                        break;
                    case 2:
                        Main.ids.get(Integer.parseInt(tID.getText())).vendedor.setSalariobase(Float.parseFloat(tSalario.getText()));
                        break;
                    case 3:
                        Main.ids.get(Integer.parseInt(tID.getText())).porpeca.setSalariobase(Float.parseFloat(tSalario.getText()));
                        break;
                    case 4:
                        Main.ids.get(Integer.parseInt(tID.getText())).porhora.setSalariobase(Float.parseFloat(tSalario.getText()));
                        break;
                    default:
                        break;
                }
            }
            switch(auxcargo){
                case 1:
                    auxganho=Main.ids.get(Integer.parseInt(tID.getText())).chefe.CalcularGanhos();
                    break;
                case 2:
                    auxganho=Main.ids.get(Integer.parseInt(tID.getText())).vendedor.CalcularGanhos(Integer.parseInt(tFator.getText()));
                    break;
                case 3:
                    auxganho=Main.ids.get(Integer.parseInt(tID.getText())).porpeca.CalcularGanhos(Integer.parseInt(tFator.getText()));
                    break;
                case 4:
                    auxganho=Main.ids.get(Integer.parseInt(tID.getText())).porhora.CalcularGanhos(Integer.parseInt(tFator.getText()));
                    break;
                default:
                    break;
            }
            JOptionPane.showMessageDialog(this, "O ganho calculado foi R$ "+auxganho+"\nPara ver sua folha de pagamento, acesse a página anterior", "Ganho mensal", 1);
        }
        else{
            JOptionPane.showMessageDialog(this, "Certifique-se que ID e cargos estejam preenchidos");
        }
        Main.gravador();
    }//GEN-LAST:event_bCadastrarActionPerformed

