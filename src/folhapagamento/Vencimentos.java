    private void bAlterarSalarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAlterarSalarioActionPerformed
        // TODO add your handling code here:
        if(Integer.parseInt(tID.getText())>=0 && Float.parseFloat(tSalario.getText())>0){
            int auxcargo=Main.ids.get(Integer.parseInt(tID.getText())).cargo;
            switch(auxcargo){
                case 1:
                    Main.ids.get(Integer.parseInt(tID.getText())).chefe.setSalariobase(Float.parseFloat(tSalario.getText()));
                    break;
                case 2:
                    Main.ids.get(Integer.parseInt(tID.getText())).vendedor.setSalariobase(Float.parseFloat(tSalario.getText()));
                    break;
                case 3:
                    Main.ids.get(Integer.parseInt(tID.getText())).porpeca.setSalariobase(Float.parseFloat(tSalario.getText()));
                    break;
                case 4:
                    Main.ids.get(Integer.parseInt(tID.getText())).porhora.setSalariobase(Float.parseFloat(tSalario.getText()));
                    break;
                default:
                    break;
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Certifique-se que ID, cargo e salário estejam preenchidos");
        }
        Main.gravador();
    }//GEN-LAST:event_bAlterarSalarioActionPerformed

